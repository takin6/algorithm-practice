# dp[i][j] = i番目までの添え字で、jを作れるか

N = int(input())
dp = [ [0]*(21) for _ in range(N) ]
lst = list(map(int,input().split()))
dp[0][0] = 1

for i in range(N-1):
  for j in range(21):
    a = lst[i]
    if i==j==a==0:
      dp[i+1][j] = 1
    else:
      if dp[i][j] > 0:
        if j-a >= 0:
          dp[i+1][j-a] += dp[i][j]
        if j+a <= 20:
          dp[i+1][j+a] += dp[i][j]

print(dp[-1][lst[-1]])

